                    Personagens Jogáveis: Durante eventos especiais, como os crossover com a Marvel, os jogadores puderam adquirir skins (visuais) de super-heróis famosos como Homem de Ferro, Capitão América, Thor, Viúva Negra, Pantera Negra, entre outros. Cada skin não apenas altera a aparência do jogador, mas muitas vezes vem com habilidades e acessórios temáticos.

